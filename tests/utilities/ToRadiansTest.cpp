#include "../pch.h"

TEST(ToRadians, ToDegrees) {
	ASSERT_FLOAT_EQ(TIE::ToRadians(0), 0);
	ASSERT_FLOAT_EQ(TIE::ToRadians(14.323945045471191), 0.25);
	ASSERT_FLOAT_EQ(TIE::ToRadians(28.647890090942383), 0.5);
	ASSERT_FLOAT_EQ(TIE::ToRadians(42.971836090087891), 0.75);
	ASSERT_FLOAT_EQ(TIE::ToRadians(57.2957801818847661), 1);
}