#include "../pch.h"

TEST(ToDegrees, ToDegrees) {
	ASSERT_DOUBLE_EQ(TIE::ToDegrees(0), 0);
	ASSERT_DOUBLE_EQ(TIE::ToDegrees(0.25), 14.323945045471191);
	ASSERT_DOUBLE_EQ(TIE::ToDegrees(0.5), 28.647890090942383);
	ASSERT_DOUBLE_EQ(TIE::ToDegrees(0.75), 42.971836090087891);
	ASSERT_DOUBLE_EQ(TIE::ToDegrees(1), 57.295780181884766);
}